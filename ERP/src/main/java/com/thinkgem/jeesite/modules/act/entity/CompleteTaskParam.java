package com.thinkgem.jeesite.modules.act.entity;

import java.io.Serializable;

/**
 * author fanjinyuan.
 */
public class CompleteTaskParam implements Serializable {

    private String processInstanceId;

    private String approval;

    private String rejectBackReason;

    private String rejectToNode; //要驳回到的节点的Key

    public String getProcessInstanceId() {
        return processInstanceId;
    }

    public void setProcessInstanceId(String processInstanceId) {
        this.processInstanceId = processInstanceId;
    }

    public String getApproval() {
        return approval;
    }

    public void setApproval(String approval) {
        this.approval = approval;
    }

    public String getRejectBackReason() {
        return rejectBackReason;
    }

    public void setRejectBackReason(String rejectBackReason) {
        this.rejectBackReason = rejectBackReason;
    }

    public String getRejectToNode() {
        return rejectToNode;
    }

    public void setRejectToNode(String rejectToNode) {
        this.rejectToNode = rejectToNode;
    }

}
