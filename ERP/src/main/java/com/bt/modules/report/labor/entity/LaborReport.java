package com.bt.modules.report.labor.entity;

import com.bt.modules.contract.entity.Laborcontract;
import com.bt.modules.houseType.entity.HouseType;
import com.bt.modules.project.entity.Project;
import com.bt.modules.project.entity.Subproject;
import com.bt.modules.report.weekly.mianly.entity.BuildingSchedule;
import com.thinkgem.jeesite.common.persistence.DataEntity;

/**
 * 劳务报表实体类
 * 用于加载字段，没有对应的数据库表
 * xiaocai
 * 2016-4-28
 */
public class LaborReport extends DataEntity<LaborReport> {

    private static final long serialVersionUID = 1L;

    
    private Project project;					//一级项目
    private Subproject subproject;				//二级项目
	private BuildingSchedule buildingSchedule;	//周报在建进度信息
	private HouseType houseType;				//户型库
	private Laborcontract laborcontract;		//劳务合同相关
    
    
    
    
    
    
    
    
    
    
    
    
    public Project getProject() {
		return project;
	}
	public void setProject(Project project) {
		this.project = project;
	}
	public Subproject getSubproject() {
		return subproject;
	}
	public void setSubproject(Subproject subproject) {
		this.subproject = subproject;
	}
	public BuildingSchedule getBuildingSchedule() {
		return buildingSchedule;
	}
	public void setBuildingSchedule(BuildingSchedule buildingSchedule) {
		this.buildingSchedule = buildingSchedule;
	}
	public HouseType getHouseType() {
		return houseType;
	}
	public void setHouseType(HouseType houseType) {
		this.houseType = houseType;
	}
	public Laborcontract getLaborcontract() {
		return laborcontract;
	}
	public void setLaborcontract(Laborcontract laborcontract) {
		this.laborcontract = laborcontract;
	}
    
	
}
