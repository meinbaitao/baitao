package com.bt.modules.receiving.entity;

import com.thinkgem.jeesite.common.persistence.DataEntity;

/**
 * 收货事故Entity
 * @author xiaocai
 * @version 2015-07-30
 */
public class ReceivingAccident extends DataEntity<ReceivingAccident> {
	
	private static final long serialVersionUID = 1L;
	private String receivingAccidentId;		// 收货事故ID
	private String isinfluence;		// 是否影响施工
	private String type;		// 事故类型
	private String description;		// 问题描述
	private String solution;		// 解决方案
	private String annex;		//上传相应的附件路径
	public String getReceivingAccidentId() {
		return receivingAccidentId;
	}
	public void setReceivingAccidentId(String receivingAccidentId) {
		this.receivingAccidentId = receivingAccidentId;
	}
	public String getIsinfluence() {
		return isinfluence;
	}
	public void setIsinfluence(String isinfluence) {
		this.isinfluence = isinfluence;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getSolution() {
		return solution;
	}
	public void setSolution(String solution) {
		this.solution = solution;
	}
	public String getAnnex() {
		return annex;
	}
	public void setAnnex(String annex) {
		this.annex = annex;
	}
	
	
}